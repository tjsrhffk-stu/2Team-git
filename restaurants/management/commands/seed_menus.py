class Command(BaseCommand):
    help = "카테고리별 메뉴를 음식점에 추가합니다."

    def handle(self, *args, **options):
        restaurants = (
            Restaurant.objects
            .filter(menu_items__isnull=True)
            .select_related("category")
            .distinct()
        )
        total = restaurants.count()
        self.stdout.write(f"메뉴 없는 음식점: {total}개 처리 시작...\n")

        created = 0
        for idx, restaurant in enumerate(restaurants.iterator(), 1):
            cat_name = restaurant.category.name if restaurant.category else "기타"
            menu_list = MENUS.get(cat_name, MENUS["기타"])

            # 메뉴 3~6개 랜덤 선택 (너무 많으면 부자연스러움)
            sample_size = random.randint(3, min(6, len(menu_list)))
            chosen = random.sample(menu_list, sample_size)

            for name, price_range, description, menu_type in chosen:
                price = random.randint(price_range[0], price_range[1])
                # 500원 단위로 반올림
                price = round(price / 500) * 500

                MenuItem.objects.create(
                    restaurant=restaurant,
                    name=name,
                    price=price,
                    description=description,
                    category=menu_type,
                    is_available=True,
                )
                created += 1

            if idx % 200 == 0:
                self.stdout.write(f"  {idx}/{total} 처리 중...")

        self.stdout.write(self.style.SUCCESS(
            f"\n✅ 완료! 총 {created}개 메뉴 생성 ({total}개 음식점)"
        ))
